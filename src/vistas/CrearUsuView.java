    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        this.usua.setNombre_usu(this.txtNombre_Crus.getText());
        this.usua.setEdad_usu(Integer.parseInt(txtEdad_Crus.getText()));
        this.usua.setCorreo_usu(this.txtCorreo_Crus.getText());
        this.usua.setContrasena_usu(this.txtContra_Crus.getText());
        this.usua.setGenero_usu(Integer.parseInt(txtgenero.getText()));
        this.usua.setIdprofesion_usu(Integer.parseInt(txtprofesion.getText()));
        this.usua.setIdtipo_usu(Integer.parseInt(txtIdTipo_Crus.getText()));
        this.usua.setIdestado_usu(Integer.parseInt(txtIdEsta_Crus.getText()));

        if (daoU.Insertar(this.usua)) {
            JOptionPane.showMessageDialog(this, "INSERTADO CON EXITO");
            this.setVisible(false);
            new LoginView().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "ERROR AL INSERTAR");
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

