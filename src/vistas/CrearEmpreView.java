    private void btnCon_CremMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCon_CremMouseClicked
        int idcate = cmbCate_Crem.getItemAt(cmbCate_Crem.getSelectedIndex()).getId_categoria();
        this.emp.setNombre_emp(this.txtNombre_Crem.getText());
        this.emp.setCorreo_emp(this.txtCorre_Crem.getText());
        this.emp.setContrasena_emp(this.txtContra_Crem.getText());
        //this.emp.setIdcategoria_emp(Integer.parseInt(this.txtcate.getText()));
        this.emp.setIdestad_emp(Integer.parseInt(this.txtIdEs_Crem.getText()));
        
        this.emp.setIdcategoria_emp(idcate);
        //this.emp.setIdcategoria_emp(new Categoria(idcate)); 

        if (this.daoE.Insertar(emp)) {
            JOptionPane.showMessageDialog(this, "INSERTADO CON EXITO");
        } else {
            JOptionPane.showMessageDialog(this, "ERROR AL INSERTAR");
        }
    }//GEN-LAST:event_btnCon_CremMouseClicked

