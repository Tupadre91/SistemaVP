    private void btnIni_logMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIni_logMouseClicked
        String pass = new String(this.jpfContra_log.getPassword());
        if (!pass.equals("") && !this.txtCorre_log.getText().equals("")) {
            this.usua.setCorreo_usu(this.txtCorre_log.getText());
            this.usua.setContrasena_usu(pass);
            //---------------------------
            this.emp.setCorreo_emp(this.txtCorre_log.getText());
            this.emp.setContrasena_emp(pass);
            //----------------------------
            if (this.txtCorre_log.getText().equals("Admin")) {
                if (this.daoU.Login(this.usua)) {
                    JOptionPane.showMessageDialog(null, "Bienvenido Administrador.");
                } else {
                    JOptionPane.showMessageDialog(null, "Datos Incorrectos.");
                }
            } else {
                if (this.daoU.Login(this.usua)) {
                    JOptionPane.showMessageDialog(null, "Bienvenido/a.");
                    this.setVisible(false);
                    new MenuUsuView().setVisible(true);

                } else if (this.daoE.Login(this.emp)) {
                    JOptionPane.showMessageDialog(null, "Empresa Bienvenida.");
                } else {
                    JOptionPane.showMessageDialog(null, "Datos Incorrectos.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese Datos.");
        }
    }//GEN-LAST:event_btnIni_logMouseClicked

