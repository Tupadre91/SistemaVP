    private void Cargar(){
        this.model.setNumRows(0);
        data.clear();
        data = (List<Profesion>) daoProf.Seleccionar();
        Object [] obj = new Object[3];
        for (Profesion fila: data) {
            obj[0] = fila.getId_profesion();
            obj[1] = fila.getNombre_prof();
            obj[2] = fila.getDemanda_prof();
            this.model.addRow(obj);
        }
        this.dgvTabla_profesion.setModel(model);
    }
    
    
